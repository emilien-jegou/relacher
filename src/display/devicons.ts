export const getIconForFile = (filename: string): string => {
  const lowercaseName = filename.toLowerCase();

  const exactMatch = getExactMatch(lowercaseName);
  if (exactMatch !== undefined) {
    return exactMatch;
  }

  const suffixMatch = getSpecialSuffixMatch(lowercaseName);
  if (suffixMatch !== undefined) {
    return suffixMatch;
  }

  // Mimics Rust's `name.split('.').next_back().unwrap_or("")`
  const parts = filename.split('.');
  const ext = parts.length > 0 ? parts[parts.length - 1] : '';

  return getExtensionMatch((ext ?? '').toLowerCase());
};

